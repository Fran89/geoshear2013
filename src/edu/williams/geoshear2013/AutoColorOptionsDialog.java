    private void setColorButtonEnablesByGroupCountSlider() {
        int numGroups = this.getNumberOfColorGroups();
        this.jButtonColor1.setEnabled(true);
        this.jButtonColor2.setEnabled(true);
        this.jButtonColor3.setEnabled(3<=numGroups);
        this.jButtonColor4.setEnabled(4<=numGroups);
        this.jButtonColor5.setEnabled(5<=numGroups);
        this.jButtonColor6.setEnabled(6<=numGroups);
        this.jButtonColor7.setEnabled(7<=numGroups);
        this.jButtonColor8.setEnabled(8<=numGroups);
        this.jButtonColor9.setEnabled(9<=numGroups);
        this.jButtonColor10.setEnabled(10<=numGroups);
        this.jButtonColor11.setEnabled(11<=numGroups);
        this.jButtonColor12.setEnabled(12<=numGroups);
    }
    
    private void handleColorChoosingForButton(javax.swing.JButton colorButton) {
        Color newColor = JColorChooser.showDialog (this,"Color Chooser",colorButton.getBackground());
        if (newColor != null)
        {
            colorButton.setBackground (newColor);
            colorButton.setForeground(Util.getContrastingTextColor(newColor));
        }
        this.repaint();
    }
    
    public Color getColorForGroupNumber(int groupNumber) {
        if (groupNumber == 1) { return this.jButtonColor1.getBackground(); }
        if (groupNumber == 2) { return this.jButtonColor2.getBackground(); }
        if (groupNumber == 3) { return this.jButtonColor3.getBackground(); }
        if (groupNumber == 4) { return this.jButtonColor4.getBackground(); }
        if (groupNumber == 5) { return this.jButtonColor5.getBackground(); }
        if (groupNumber == 6) { return this.jButtonColor6.getBackground(); }
        if (groupNumber == 7) { return this.jButtonColor7.getBackground(); }
        if (groupNumber == 8) { return this.jButtonColor8.getBackground(); }
        if (groupNumber == 9) { return this.jButtonColor9.getBackground(); }
        if (groupNumber == 10) { return this.jButtonColor10.getBackground(); }
        if (groupNumber == 11) { return this.jButtonColor11.getBackground(); }
        if (groupNumber == 12) { return this.jButtonColor12.getBackground(); }
        return this.jButtonColor1.getBackground();
    }
    
